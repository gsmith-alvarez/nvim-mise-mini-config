-- Robust Quote Fetcher (Prevents Neovim from freezing if offline)
local function get_quote()
    -- The --max-time 2 ensures Neovim doesn't hang for more than 2 seconds if the API is slow
    local handle = io.popen("curl -s --max-time 2 https://api.adviceslip.com/advice 2>/dev/null")
    if not handle then return '"The only way to go fast is to go well."' end

    local result = handle:read("*a")
    handle:close()

    if result and result ~= "" then
        local quote = result:match('"advice"%s*:%s*"(.-)"')
        if quote then
            return '"' .. quote .. '"'
        end
    end

    return '"The only way to go fast is to go well."' -- Fallback
end

